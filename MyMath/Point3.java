package MyMath;

public class Point3 {
  public double x;
  public double y;
  public double z;
  public Point3(double x, double y, double z){
    this.x = x;
    this.y = y;
    this.z = z;
  }
  public Double getX() {
    return x;
  }
  public void setX(double x) {
    this.x = x;
  }
  public Double getY() {
    return y;
  }
  public void setY(double y) {
    this.y = y;
  }
  public Double getZ() {
    return z;
  }
  public void setZ(double z) {
    this.z = z;
  } 
  
}
